import random

# यह आपके 200 सवालों का मुख्य हिस्सा है
questions = [
    {"q": "भारत का राष्ट्रीय खेल क्या है?", "o": ["क्रिकेट", "हॉकी", "कब्र्डी", "फुटबॉल"], "a": "हॉकी"},
    {"q": "सौरमंडल का सबसे बड़ा ग्रह कौन सा है?", "o": ["मंगल", "पृथ्वी", "बृहस्पति", "शनि"], "a": "बृहस्पति"},
    {"q": "उत्तराखंड की राजधानी कहाँ है?", "o": ["नैनीताल", "देहरादून", "हरिद्वार", "रुड़की"], "a": "देहरादून"},
    {"q": "कंप्यूटर का दिमाग किसे कहा जाता है?", "o": ["Mouse", "Keyboard", "CPU", "Monitor"], "a": "CPU"},
    {"q": "इंद्रधनुष में कितने रंग होते हैं?", "o": ["5", "6", "7", "8"], "a": "7"},
    # पंकज जी, आप इसी तरह 200 सवाल जोड़ सकते हैं...
]

def play_game():
    print("--- स्वागत है इशिका और प्रिंस का KBC गेम में! ---")
    score = 0
    random.shuffle(questions) # हर बार नए सवाल आएंगे
    
    for i, item in enumerate(questions[:20]): # एक बार में 20 सवाल
        print(f"\nसवाल {i+1}: {item['q']}")
        for j, opt in enumerate(item['o']):
            print(f"{j+1}. {opt}")
        
        try:
            choice = int(input("सही विकल्प नंबर चुनें: "))
            if item['o'][choice-1] == item['a']:
                print("✅ बिल्कुल सही जवाब!")
                score += 10
            else:
                print(f"❌ गलत! सही जवाब था: {item['a']}")
        except:
            print("कृपया सिर्फ नंबर (1,2,3,4) लिखें।")

    print(f"\nगेम खत्म! आपका कुल स्कोर: {score}")

if __name__ == "__main__":
    play_game()
